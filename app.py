import streamlit as st
import cv2
import numpy as np
from PIL import Image
import io

# ‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤‡∏´‡∏ô‡πâ‡∏≤‡πÄ‡∏ß‡πá‡∏ö
st.set_page_config(page_title="Gemini Watermark Remover", page_icon="‚ú®")

st.title("‚ú® Gemini Watermark Remover (Pro)")
st.markdown("‡∏ñ‡πâ‡∏≤‡∏•‡∏ö‡πÅ‡∏•‡πâ‡∏ß‡∏¢‡∏±‡∏á‡πÄ‡∏´‡∏•‡∏∑‡∏≠‡πÄ‡∏®‡∏© ‡πÉ‡∏´‡πâ‡∏õ‡∏£‡∏±‡∏ö '‡∏Ç‡∏ô‡∏≤‡∏î‡∏û‡∏∑‡πâ‡∏ô‡∏ó‡∏µ‡πà‡∏•‡∏ö' ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏Ç‡∏∂‡πâ‡∏ô‡∏ô‡∏∞‡∏Ñ‡∏£‡∏±‡∏ö")

# ‡∏™‡πà‡∏ß‡∏ô‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡πÑ‡∏ü‡∏•‡πå
uploaded_file = st.file_uploader("‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏£‡∏π‡∏õ‡∏†‡∏≤‡∏û (JPG/PNG)", type=["jpg", "jpeg", "png"])

if uploaded_file is not None:
    # 1. ‡πÄ‡∏ï‡∏£‡∏µ‡∏¢‡∏°‡∏†‡∏≤‡∏û
    image = Image.open(uploaded_file).convert("RGB")
    img_array = np.array(image)
    h, w = img_array.shape[:2]

    # --- ‡∏™‡πà‡∏ß‡∏ô‡∏õ‡∏£‡∏±‡∏ö‡πÅ‡∏ï‡πà‡∏á (Settings) ---
    st.write("---")
    st.write("### üõ†Ô∏è ‡∏õ‡∏£‡∏±‡∏ö‡∏à‡∏π‡∏ô‡∏Å‡∏≤‡∏£‡∏•‡∏ö")
    
    col1, col2 = st.columns(2)
    with col1:
        # ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏Ç‡∏ô‡∏≤‡∏î Default ‡∏à‡∏≤‡∏Å 65 -> 100 ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÉ‡∏´‡πâ‡∏Å‡∏¥‡∏ô‡∏û‡∏∑‡πâ‡∏ô‡∏ó‡∏µ‡πà‡∏Å‡∏ß‡πâ‡∏≤‡∏á‡∏Ç‡∏∂‡πâ‡∏ô
        mask_size = st.slider("‡∏Ç‡∏ô‡∏≤‡∏î‡∏û‡∏∑‡πâ‡∏ô‡∏ó‡∏µ‡πà‡∏•‡∏ö (Mask Size)", 50, 200, 100, help="‡∏ñ‡πâ‡∏≤‡∏¢‡∏±‡∏á‡∏°‡∏µ‡πÄ‡∏®‡∏©‡∏•‡∏≤‡∏¢‡∏ô‡πâ‡∏≥‡πÄ‡∏´‡∏•‡∏∑‡∏≠ ‡πÉ‡∏´‡πâ‡πÄ‡∏•‡∏∑‡πà‡∏≠‡∏ô‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏Ç‡∏∂‡πâ‡∏ô")
    with col2:
        # ‡∏õ‡∏£‡∏±‡∏ö‡∏£‡∏∞‡∏¢‡∏∞‡∏Ç‡∏≠‡∏ö (‡πÄ‡∏ú‡∏∑‡πà‡∏≠‡∏•‡∏≤‡∏¢‡∏ô‡πâ‡∏≥‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ‡∏ä‡∏¥‡∏î‡∏Ç‡∏≠‡∏ö‡πÄ‡∏õ‡πä‡∏∞‡πÜ)
        offset = st.slider("‡∏£‡∏∞‡∏¢‡∏∞‡∏´‡πà‡∏≤‡∏á‡∏à‡∏≤‡∏Å‡∏Ç‡∏≠‡∏ö (Offset)", 0, 50, 10)

    # 2. ‡∏™‡∏£‡πâ‡∏≤‡∏á Mask ‡∏ï‡∏≤‡∏°‡∏Ñ‡πà‡∏≤‡∏ó‡∏µ‡πà‡∏õ‡∏£‡∏±‡∏ö
    mask = np.zeros(img_array.shape[:2], dtype=np.uint8)
    
    # ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á‡∏°‡∏∏‡∏°‡∏Ç‡∏ß‡∏≤‡∏•‡πà‡∏≤‡∏á
    start_x = w - mask_size - offset
    start_y = h - mask_size - offset
    end_x = w - offset
    end_y = h - offset
    
    # ‡∏ß‡∏≤‡∏î‡∏™‡∏µ‡πà‡πÄ‡∏´‡∏•‡∏µ‡πà‡∏¢‡∏°‡∏ó‡∏µ‡πà‡∏à‡∏∞‡∏•‡∏ö (‡πÅ‡∏™‡∏î‡∏á‡πÉ‡∏´‡πâ user ‡πÄ‡∏´‡πá‡∏ô‡∏î‡πâ‡∏ß‡∏¢‡∏ß‡πà‡∏≤‡∏•‡∏ö‡∏ï‡∏£‡∏á‡πÑ‡∏´‡∏ô)
    # ‡πÅ‡∏ï‡πà‡∏ï‡∏≠‡∏ô‡∏•‡∏ö‡∏à‡∏£‡∏¥‡∏á‡πÉ‡∏ä‡πâ mask ‡∏Ç‡∏≤‡∏ß‡∏î‡∏≥
    cv2.rectangle(mask, (start_x, start_y), (end_x, end_y), 255, -1)

    # 3. ‡∏õ‡∏£‡∏∞‡∏°‡∏ß‡∏•‡∏ú‡∏•‡∏•‡∏ö (Inpainting)
    # ‡πÉ‡∏ä‡πâ Radius ‡πÉ‡∏´‡∏ç‡πà‡∏Ç‡∏∂‡πâ‡∏ô‡πÄ‡∏•‡πá‡∏Å‡∏ô‡πâ‡∏≠‡∏¢ (5) ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡πÄ‡∏ô‡∏µ‡∏¢‡∏ô
    result = cv2.inpaint(img_array, mask, 5, cv2.INPAINT_TELEA)

    # 4. ‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå
    st.write("---")
    st.success("‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå (‡∏õ‡∏£‡∏±‡∏ö Slider ‡∏î‡πâ‡∏≤‡∏ô‡∏ö‡∏ô‡πÑ‡∏î‡πâ‡πÄ‡∏•‡∏¢ ‡∏†‡∏≤‡∏û‡∏à‡∏∞‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ó‡∏±‡∏ô‡∏ó‡∏µ)")
    
    # ‡πÅ‡∏™‡∏î‡∏á‡∏†‡∏≤‡∏û‡πÄ‡∏ó‡∏µ‡∏¢‡∏ö‡∏Å‡∏±‡∏ô‡∏ä‡∏±‡∏î‡πÜ
    c1, c2 = st.columns(2)
    with c1:
        # ‡∏ß‡∏≤‡∏î‡∏Å‡∏£‡∏≠‡∏ö‡∏™‡∏µ‡πÅ‡∏î‡∏á‡∏•‡∏á‡πÉ‡∏ô‡∏†‡∏≤‡∏û‡∏ï‡πâ‡∏ô‡∏â‡∏ö‡∏±‡∏ö ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÉ‡∏´‡πâ‡∏£‡∏π‡πâ‡∏ß‡πà‡∏≤‡πÄ‡∏£‡∏≤‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏•‡∏ö‡∏ï‡∏£‡∏á‡πÑ‡∏´‡∏ô
        preview_img = img_array.copy()
        cv2.rectangle(preview_img, (start_x, start_y), (end_x, end_y), (255, 0, 0), 2)
        st.image(preview_img, caption="‡∏û‡∏∑‡πâ‡∏ô‡∏ó‡∏µ‡πà‡∏™‡∏µ‡πÅ‡∏î‡∏á‡∏Ñ‡∏∑‡∏≠‡∏™‡πà‡∏ß‡∏ô‡∏ó‡∏µ‡πà‡∏à‡∏∞‡∏ñ‡∏π‡∏Å‡∏•‡∏ö", use_column_width=True)
        
    with c2:
        st.image(result, caption="‚ú® ‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå‡∏ó‡∏µ‡πà‡πÑ‡∏î‡πâ", use_column_width=True)

    # 5. ‡∏õ‡∏∏‡πà‡∏°‡∏î‡∏≤‡∏ß‡∏ô‡πå‡πÇ‡∏´‡∏•‡∏î
    result_pil = Image.fromarray(result)
    buf = io.BytesIO()
    result_pil.save(buf, format="PNG")
    byte_im = buf.getvalue()

    st.download_button(
        label="üì• ‡∏î‡∏≤‡∏ß‡∏ô‡πå‡πÇ‡∏´‡∏•‡∏î‡∏£‡∏π‡∏õ‡∏†‡∏≤‡∏û",
        data=byte_im,
        file_name="cleaned_image.png",
        mime="image/png",
        type="primary"
    )